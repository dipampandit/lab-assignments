# Various primitive datatypes in Python
integer_num = 10        # int
floating_num = 3.14     # float
string_text = "Hello"   # str
boolean_val = True      # bool
complex_num = 2 + 3j    # complex

print("Integer:", integer_num)
print("Float:", floating_num)
print("String:", string_text)
print("Boolean:", boolean_val)
print("Complex:", complex_num)
