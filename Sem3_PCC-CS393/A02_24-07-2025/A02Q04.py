real1 = float(input("Enter real part of 1st complex number: "))
imag1 = float(input("Enter imaginary part of 1st complex number: "))
real2 = float(input("Enter real part of 2nd complex number: "))
imag2 = float(input("Enter imaginary part of 2nd complex number: "))

z1 = complex(real1, imag1)
z2 = complex(real2, imag2)

print("Addition:", z1 + z2)
print("Subtraction:", z1 - z2)
