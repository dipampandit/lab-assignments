# Converting between types
num_str = "123"
num_int = int(num_str)     # string → int
num_float = float(num_str) # string → float

print("String to int:", num_int)
print("String to float:", num_float)
print("Float to int:", int(3.99))   # converts 3.99 → 3
print("Int to string:", str(100))  # converts 100 → "100"
