a = 1000
b = 1000

print("a == b :", a == b)     # Checks value equality
print("a is b :", a is b)     # Checks if both refer to same object

# Arithmetic example
c = 10 + 5
d = 15
print("c == d:", c == d)      # True (values same)
print("c is d:", c is d)      # False (same value but different object)
print("c is not d:", c is not d)      # False
